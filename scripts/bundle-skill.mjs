#!/usr/bin/env node
// Bundle the vibeloop CLI into a single self-contained file shipped inside the
// Skill, so the Skill works as a product copy outside this monorepo.
//
// Output: skills/vibeloop-harness/vendor/vibeloop.mjs (+ VERSION sibling).
// The vendor dir is gitignored — this is a build/release step, not a committed
// artifact. Run after `pnpm build` (bundles packages/cli/dist).
//
// esbuild is invoked via `npx -y esbuild@<pinned>` so this release step needs
// no declared dependency: it works on a clean checkout without touching
// package.json / the lockfile. `VIBELOOP_ESBUILD` overrides the launcher
// (e.g. "pnpm exec esbuild") for offline use.
import { spawn } from 'node:child_process';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ESBUILD_VERSION = '0.28.1';
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const entry = path.join(repoRoot, 'packages/cli/bin/vibeloop');
const vendorDir = path.join(repoRoot, 'skills/vibeloop-harness/vendor');
const outfile = path.join(vendorDir, 'vibeloop.mjs');

const cliPkg = JSON.parse(
  await readFile(path.join(repoRoot, 'packages/cli/package.json'), 'utf8')
);
const version = cliPkg.version;

// The graph mixes ESM with CJS deps that call require(); define a real require
// for the ESM output so dynamic requires (e.g. node:events) resolve at runtime.
const banner = [
  '// vibeloop CLI bundle — generated by scripts/bundle-skill.mjs (do not edit)',
  `// cli_version: ${version}`,
  "import { createRequire as __vibeloopCreateRequire } from 'node:module';",
  'const require = __vibeloopCreateRequire(import.meta.url);'
].join('\n');

await mkdir(vendorDir, { recursive: true });

const esbuildArgs = [
  entry,
  '--bundle',
  '--platform=node',
  '--format=esm',
  '--target=node22',
  `--banner:js=${banner}`,
  `--outfile=${outfile}`
];

// Launcher: `npx -y esbuild@x` by default; or split VIBELOOP_ESBUILD override.
const override = process.env.VIBELOOP_ESBUILD;
const [launcher, ...launcherArgs] = override
  ? override.split(' ')
  : ['npx', '-y', `esbuild@${ESBUILD_VERSION}`];

const code = await new Promise((resolve, reject) => {
  const child = spawn(launcher, [...launcherArgs, ...esbuildArgs], {
    cwd: repoRoot,
    stdio: 'inherit'
  });
  child.on('error', reject);
  child.on('close', resolve);
});
if (code !== 0) {
  throw new Error(`esbuild bundle failed with exit code ${code}`);
}

await writeFile(path.join(vendorDir, 'VERSION'), `${version}\n`);
console.log(
  `bundled vibeloop CLI v${version} -> ${path.relative(repoRoot, outfile)}`
);
